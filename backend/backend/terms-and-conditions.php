	<li><strong>Air Waybill (AWB)</strong> – The document which covers transport by air. It is issued by the carrier, whether an airline or a freight forwarder, as a non-negotiable document serving as a receipt to the consignor for the goods, and containing the conditions of transport. It also shows the details of the consignee so that they can be contacted on arrival of the goods.</li>
	<li><strong>Agent</strong> - An independent person or corporation acting as a representative, usually in a foreign market, who attempts to sell products for an overseas seller (principal) and earns a commission on successful sales. Agents are not normally involved in delivery or servicing of product.</li>
	<li><strong>BAF </strong>– Bunker Adjustment Factor - an adjustment to shipping companies' freight rates to take into account fluctuations in the cost of fuel oil (bunkers) for their ships.</li>
	<li><strong>Beneficiary</strong> -The seller in whose favour an L/C is issued, ie the person who will "benefit" from the L/C.</li>
	<li><strong>Bill of Lading (B/L)</strong>: The document which covers transport by sea. Signed by the carrier, whether a shipping line or a freight forwarder, it serves as a receipt to the consignor for the goods, as evidence of the contract of transport containing the conditions of transport, and as a document of title by which possession of the goods can be transferred. Typically a B/L is issued in a set of three signed originals or negotiables, one of which must be presented to claim the goods upon which the others become void.</li>
	<li><strong>Carnet</strong> - A document, normally issued by a Chamber of Commerce which is a member of the International Chamber of Commerce (ICC) to enable the holder to temporarily take merchandise into certain countries, as samples or for display purposes, without the need to pay import duty or pay a bond for the duty. The issuer will require the holder to give them security by way of a bank guarantee.</li>
	<li><strong>Certificate of Origin</strong> - A certificate stating the country of origin of the goods. Depending on the importing country's requirements, this can be as simple as being issued by the seller or the manufacturer. In most cases however, it is required to be issued by a Chamber of Commerce in the country of origin.</li>
	<li><strong>Commercial Invoice</strong> - A document issued by the seller, addressed to the buyer, giving details of the individual transaction, including complete description of the goods, prices, currency, delivery and payment terms and so on. This is generally used by the Customs authorities in the importing country to assess customs duties payable.</li>
	<li><strong>Customs Duty</strong> - A tax, duty or tariff levied at the time of import upon goods entering a country. Usually based on the value of the goods (ad valorem), on the physical nature of the goods such as quantity or weight, or on a combination of the value and other factors.</li>
